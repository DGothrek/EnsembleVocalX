



<p>
L'Ensemble Vocal de l'École polytechnique regroupe 60 chanteurs amateurs : 
élèves polytechniciens, internationaux, anciens élèves, personnels de l'école
ou des laboratoires... <br>
</p>
<p>
Dirigé par le chef de choeur Patrice Holiner,
il se produit régulièrement dans des lieux prestigieux,
interprétant principalement des œuvres classiques du chant choral.
Ainsi, il a dernièrement eu l'occasion de donner en concert
le Messie de Haendel, la Petite Messe Solennelle de Rossini ou encore le
Requiem Allemand de Brahms. <br>
</p>
<p>
La chorale participe également régulièrement à des actions solidaires,
en donnant des concerts au profit d'associations telles que LyricAutsim
(cathédrale d'Evry en février 2010), l'Ordre de Malte
(Grand Amphithéâtre de la Sorbonne en mars 2011) et l'Association Petits Princes
(église Saint-Eustache en décembre 2012), la fondation d'Auteuil...
</p>